    {
        //
       $request->validate([
           'message' => 'required',
       ]);
       $comment = new Comment();
       $comment->user_id = $iduser;
       $comment->game_id = $id;
       $comment->message = $request->message;
       $comment->save();
       return redirect('detail/'.$id);
       
    }

